const filterNavItemsByRole = (navItems, role) => {
  if (role === 'superClient') {
    return navItems
  } else if (role === 'client') {
    // Filter out admin-specific items for clients
    return navItems.filter((item) => {
      if (item.name === 'Users') return false
      return true
    })
  }
  return navItems
}

